using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator__CLI_
{
    /*
     * Class that provides method for doing operations on numbers or get math constants.
     */
    class Math
    {
        //  Addition operation.
        public static double Add(double numberA, double numberB)
        {
            return numberA + numberB;
        }

        //  Substraction operation.
        public static double Substract(double numberA, double numberB)
        {
            return numberA - numberB;
        }
    }
}
